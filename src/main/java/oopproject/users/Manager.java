package oopproject.users;

import java.time.LocalDate;
import java.util.ArrayList;
import java.util.Comparator;
import java.util.List;
import oopproject.academic.Course;
import oopproject.academic.News;
import oopproject.academic.Report;
import oopproject.academic.Request;
import oopproject.core.University;
import oopproject.enums.ManagerType;
import oopproject.enums.RequestStatus;
import oopproject.enums.ReportType;
import oopproject.enums.UserType;
import oopproject.exceptions.LowHIndexException;
import oopproject.research.ResearchService;
import oopproject.research.Researcher;

public class Manager extends Employee {
    private ManagerType type;
    private final List<News> news = new ArrayList<>();

    public Manager() {
        this.role = UserType.MANAGER;
    }

    public Manager(int id, String username, String password, String email,
                   double salary, LocalDate hireDate, ManagerType type) {
        super(id, username, password, email, salary, hireDate);
        this.role = UserType.MANAGER;
        this.type = type;
    }

    public void approveRegistration(Employee employee) {
        if (employee != null) {
            University.getInstance().addLog(this, "REGISTRATION_APPROVED employeeId=" + employee.getId());
        }
    }

    public void rejectRegistration(Employee employee) {
        if (employee != null) {
            University.getInstance().addLog(this, "REGISTRATION_REJECTED employeeId=" + employee.getId());
        }
    }

    public void assignTeacher(Course course, Teacher teacher) {
        teacher.addCourse(course);
    }

    public Report generateReport() {
        return new Report(ReportType.COURSE_STATISTICS, "Generated by " + username);
    }

    public Report generateAcademicPerformanceReport() {
        List<Student> students = University.getInstance().getStudents();
        if (students.isEmpty()) {
            return new Report(ReportType.COURSE_STATISTICS, "No students found.");
        }

        double averageGpa = students.stream()
                .mapToDouble(student -> student.getTranscript().calculateGpa())
                .average()
                .orElse(0.0);
        long atRiskCount = students.stream()
                .filter(student -> student.getTranscript().calculateGpa() < 2.0)
                .count();
        Student topStudent = students.stream()
                .max(Comparator.comparingDouble((Student student) -> student.getTranscript().calculateGpa()))
                .orElse(null);

        String data = "Average GPA: " + String.format(java.util.Locale.US, "%.2f", averageGpa)
                + ", at risk students: " + atRiskCount
                + ", top student: " + (topStudent == null
                ? "n/a"
                : topStudent.getUsername() + " ("
                + String.format(java.util.Locale.US, "%.2f", topStudent.getTranscript().calculateGpa()) + ")");
        Report report = new Report(ReportType.COURSE_STATISTICS, data);
        University.getInstance().addLog(this, "ACADEMIC_REPORT_GENERATED");
        return report;
    }

    public void createCourse(Course course) {
        course.setOpen(false);
    }

    public void openCourseForRegistration(Course course) {
        course.setOpen(true);
    }

    public boolean createCourseForRegistration(String name, String code, int credits,
                                               String targetMajor, Integer targetYear) {
        Course course = new Course(name, code, credits, targetMajor, targetYear);
        createCourse(course);
        return University.getInstance().addCourse(course);
    }

    public boolean setCourseRegistrationStatus(Course course, boolean open) {
        if (course == null) {
            return false;
        }
        if (open) {
            openCourseForRegistration(course);
        } else {
            course.setOpen(false);
        }
        University.getInstance().addLog(this, (open ? "COURSE_OPENED " : "COURSE_CLOSED ") + course.getCode());
        return true;
    }

    public boolean assignTeacherToCourse(Teacher teacher, Course course) {
        if (teacher == null || course == null) {
            return false;
        }
        assignTeacher(course, teacher);
        University.getInstance().addLog(this, "TEACHER_ASSIGNED " + course.getCode());
        return true;
    }

    public void createNews(News item) {
        news.add(item);
    }

    public void manageNews(News item) {
        if (!news.contains(item)) {
            news.add(item);
        }
    }

    public boolean broadcastNews(String title, String content) {
        if (title == null || title.isBlank() || content == null || content.isBlank()) {
            return false;
        }
        News item = new News(title, content, LocalDate.now(), getUsername());
        createNews(item);
        University.getInstance().getUsers().forEach(user -> user.receiveNews(item));
        University.getInstance().addLog(this, "NEWS_BROADCAST " + title);
        return true;
    }

    public List<Student> getStudentsByGpa() {
        return University.getInstance().getStudents().stream()
                .sorted(Comparator.comparingDouble(Student::getGpa).reversed())
                .toList();
    }

    public List<Student> getStudentsAlphabetically() {
        return University.getInstance().getStudents().stream()
                .sorted(Comparator.comparing(Student::getUsername))
                .toList();
    }

    public List<Student> getResearcherStudents() {
        return University.getInstance().getStudents().stream()
                .filter(Student::isResearcher)
                .sorted(Comparator.comparing(Student::getUsername))
                .toList();
    }

    public List<Teacher> getTeachersAlphabetically() {
        return University.getInstance().getTeachers().stream()
                .sorted(Comparator.comparing(Teacher::getUsername))
                .toList();
    }

    public List<Teacher> getTeachersByHIndex() {
        return University.getInstance().getTeachers().stream()
                .sorted(Comparator.comparingInt(Researcher::calculateHIndex).reversed())
                .toList();
    }

    public boolean assignResearchSupervisor(Student student, Researcher supervisor) throws LowHIndexException {
        if (student == null || supervisor == null || !student.isResearcher() || !supervisor.isResearcher()) {
            return false;
        }
        ResearchService.assignSupervisor(student, supervisor);
        University.getInstance().addLog(this,
                "SUPERVISOR_ASSIGNED studentId=" + student.getId()
                        + " supervisor=" + supervisor.getResearcherName());
        return true;
    }

    public List<Request> showRequests() {
        return University.getInstance().getRequests();
    }

    public List<Request> showPendingRequests() {
        return showRequests().stream()
                .filter(request -> request.getStatus() == RequestStatus.PENDING)
                .toList();
    }

    public Request findRequestById(long requestId) {
        return University.getInstance().findRequestById(requestId).orElse(null);
    }

    public boolean approveRequest(Request request) {
        if (!isManagedRequest(request)) {
            return false;
        }
        request.approve();
        University.getInstance().addLog(this, "REQUEST_APPROVED id=" + request.getRequestId());
        return true;
    }

    public boolean approveRequest(long requestId) {
        return approveRequest(findRequestById(requestId));
    }

    public boolean rejectRequest(Request request) {
        if (!isManagedRequest(request)) {
            return false;
        }
        request.reject();
        University.getInstance().addLog(this, "REQUEST_REJECTED id=" + request.getRequestId());
        return true;
    }

    public boolean rejectRequest(long requestId) {
        return rejectRequest(findRequestById(requestId));
    }

    public boolean commentRequest(Request request, String comment) {
        if (!isManagedRequest(request) || comment == null || comment.isBlank()) {
            return false;
        }
        request.setResponseComment(comment);
        University.getInstance().addLog(this, "REQUEST_COMMENTED id=" + request.getRequestId());
        return true;
    }

    public boolean commentRequest(long requestId, String comment) {
        return commentRequest(findRequestById(requestId), comment);
    }

    public boolean removeRequest(long requestId) {
        return University.getInstance().removeRequest(this, requestId);
    }

    public List<Request> showFinishedRequests() {
        return University.getInstance().getFinishedRequests();
    }

    private boolean isManagedRequest(Request request) {
        return request != null && University.getInstance().findRequestById(request.getRequestId()).isPresent();
    }
}
